account = [
    {"날짜": "2025-04-02", "분류": "지출", "내용": "커피", "금액": 3000},
    {"날짜": "2025-04-03", "분류": "수입", "내용": "월급", "금액": 2000000},
    {"날짜": "2025-04-03", "분류": "지출", "내용": "점심", "금액": 8000}
]

print("📋 가계부 항목 목록\n------------------------")
if len(account) == 0:
    print("현재 저장된 항목이 없습니다.")
elif len(account) >= 5:
    sorted_account = sorted(account, key=lambda x: x['날짜'], reverse=True)
    for i in sorted_account[-5:]:
        print(f"날짜: {i['날짜']} | 분류: {i['분류']} | 내용: {i['내용']} | 금액: {i['금액']}원")
else:
    for i in account:
        print(f"날짜: {i['날짜']} | 분류: {i['분류']} | 내용: {i['내용']} | 금액: {i['금액']}원")
print("------------------------")